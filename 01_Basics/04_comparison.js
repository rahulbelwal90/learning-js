// Comparison Operator
console.log(2>1)   //greater than
console.log(2<1)   //less than
console.log(2>=1)  //greater equal to
console.log(2<=1)  //less than equal to
console.log(2==2)  //equal to
console.log(2!=1)  //not equal to

// === triple equal also compare their data type with their values
console.log("2" === 1);
console.log("5" === "5");
console.log(2 === 2);


